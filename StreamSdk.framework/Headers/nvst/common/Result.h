    // 0               1               2               3
    // 0 1 2 3 4 5 6 7 0 1 2 3 4 5 6 7 0 1 2 3 4 5 6 7 0 1 2 3 4 5 6 7
    // +-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
    // |E|Reserved Bits|      EG       |      Status Code(SC)          |
    // +-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
    //
    // Error Bit (E): 1 bit
    //                 If 1 then it's an error, otherwise success.
    //
    // Reserved Bits(RC): 7 bits
    //                 These bits are reserved for possible future expansion.
    //
    // Error Group(EG): 8 bits
    //                 This will include various errors groups like NVSC,
    //                 Decoder, ENET, JNI, JAVA, PGC, RTSP, ENET, etc.
    //                 2^8 is large enough to hold any number of modules that we define.
    //
    // Status Code(SC): 16 bits
    //                 This will include module specific error codes.
    //                 These are defined in their respective module.
    //                 For example, from NVSC these will include disconnect reasons,
    //                 network error codes, connection attempt error codes, etc.

    /// Error groups.
    /// Last 16 bits of the module code should be zero.
    /// THESE MAPPINGS ARE FIXED AND SHOULD NOT BE CHANGED.
    /// IF REQUIRED YOU CAN APPEND NEW ERROR GROUP AFTER THE LAST.
    typedef enum NvstErrorGroups_t
    {
        /// Generic errors unrelated to a particular module.
        NEG_NVST_GENERIC = 0x00,
        // NVSC related errors.
        // This will start with error group 0x01.
        // Reserving 0x01-0x1F for NVSC reasons.
        /// First NVSC error group.
        NEG_NVSC_FIRST = 0x01,
        /// Errors during initial handshake.
        NEG_NVSC_CONNECTION_ATTEMPT = 0x02,
        /// during streaming.
        NEG_NVSC_SERVER_DISCONNECT = 0x03,
        /// Network errors during streaming.
        NEG_NVSC_NETWORK = 0x04,
        /// Audio QoS errors.
        NEG_NVSC_AUDIO_QOS = 0x05,
        /// Video QoS errors.
        NEG_NVSC_VIDEO_QOS = 0x06,
        /// Enet releated errors.
        NEG_NVSC_ENET = 0x07,
        /// RTSP related errors.
        NEG_NVSC_RTSP = 0x08,
        /// RTP related errors.
        NEG_NVSC_RTP = 0x09,
        /// FEC errors.
        NEG_NVSC_FEC = 0x0A,
        /// NVSC generic errors.
        NEG_NVSC_GENERIC = 0x0B,
        /// Errors in the server control module.
        NEG_NVSC_SERVER_CONTROL = 0x0C,
        /// Errors due to remote input.
        NEG_NVSC_REMOTE_INPUT = 0x0D,
        /// Errors due to video frame provider, encoder, adapter, pipeline.
        NEG_NVSC_SERVER_VIDEO_UNINTENDED = 0x0E,
        /// Errors due to audio pipeline on server.
        NEG_NVSC_SERVER_AUDIO_UNINTENDED = 0x0F,
        /// Errors due to audio pipeline on client.
        NEG_NVSC_CLIENT_AUDIO_UNINTENDED = 0x10,
        /// Intended client disconnection reasons.
        NEG_NVSC_CLIENT_INTENDED = 0x11,
        /// Last NVSC error group.
        NEG_NVSC_LAST = 0x1F,

        // Reserved for Gamestream Services
        /// Reserved for services (first).
        NEG_RESERVED_FOR_SERVICES_FIRST = 0x20,
        /// Reserved for services (last).
        NEG_RESERVED_FOR_SERVICES_LAST = 0x2F,

        // Reserved for clients to NVSC.
        /// Reserved for clients to NVSC (first).
        NEG_NVSC_RESERVED_FOR_CLIENT_FIRST = 0x30,
        /// Reserved for client to NVSC (last).
        NEG_NVSC_RESERVED_FOR_CLIENT_LAST = 0x3F,

        // NVSS related errors.
        /// The NVSS group range goes from 0x40 to 0x4F.
        NEG_NVSS_FIRST = 0x40,
        /// Initialization related errors.
        NEG_NVSS_INIT = 0x40,
        /// Placeholder for the next error group.
        NEG_NVSS_PLACEHOLDER = 0x41,
        /// Errors during initial handshake.
        NEG_NVSS_CONNECTION_ATTEMPT = 0x42,
        /// Errors during streaming.
        NEG_NVSS_CLIENT_DISCONNECT = 0x43,
        /// Network errors during streaming.
        NEG_NVSS_NETWORK = 0x44,
        /// Audio QOS Errors.
        NEG_NVSS_AUDIO_QOS = 0x45,
        /// Video QOS Errors.
        NEG_NVSS_VIDEO_QOS = 0x46,
        /// Enet releated errors.
        NEG_NVSS_ENET = 0x47,
        /// RTSP related errors.
        NEG_NVSS_RTSP = 0x48,
        // Please replace NEG_NVSS_PLACEHOLDER (0x41) before using 0x49
        // then delete this comment.
        /// Last NVSS error group.
        NEG_NVSS_LAST = 0x4F,
    } NvstErrorGroups;

